#ifndef USER_CODE_SERVO_H_
#define USER_CODE_SERVO_H_


//-------------------------------------------------------------------------------------------------------------------
//  @brief      舵机模块初始化
//  @param      void            无
//  @return     void            无
//  @since      v1.0
//  Sample usage: 在 main 函数开始时调用一次 servo_init();
//  @note       本函数会初始化一个通用定时器PWM模块，频率为50Hz，专门用于舵机控制。
//              请根据实际硬件连接，在 servo.c 文件中修改 SERVO_PWM_CH 定义。
//-------------------------------------------------------------------------------------------------------------------
void servo_init(void);


//-------------------------------------------------------------------------------------------------------------------
//  @brief      设置舵机角度
//  @param      angle           目标角度，范围为 0 到 180 度。超出范围的值会被限制。
//  @return     void            无
//  @since      v1.0
//  Sample usage: set_servo_angle(90); // 让舵机转到中间位置
//  @note       本函数将输入的角度值（0-180）精确映射为PWM占空比，以控制舵机转动。
//-------------------------------------------------------------------------------------------------------------------
void set_servo_angle(int angle);



#endif /* USER_CODE_SERVO_H_ */
